namespace ICG.AspNetCore.Utilities.Bootstrap5TagHelpers.Form;

/// <summary>
/// Representation of the supported form control sizes within Bootstrap
/// </summary>
public enum BootstrapFormControlSize
{
    /// <summary>
    /// The standard form control size
    /// </summary>
    Standard = 1,
    /// <summary>
    /// Larger form control display
    /// </summary>
    Lg = 2,
    /// <summary>
    /// Smaller form control display
    /// </summary>
    Sm = 3
}
