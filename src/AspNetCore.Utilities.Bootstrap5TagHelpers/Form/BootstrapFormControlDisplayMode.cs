namespace ICG.AspNetCore.Utilities.Bootstrap5TagHelpers.Form;

/// <summary>
///     An indicator of the method of form display
/// </summary>
public enum BootstrapFormControlDisplayMode
{
    /// <summary>
    ///     The traditional method for displaying a form control, with the label above the input
    /// </summary>
    Standard = 1,

    /// <summary>
    ///     An inline display of the form control only
    /// </summary>
    Inline = 2
}